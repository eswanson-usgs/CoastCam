CREATE TABLE camera(
id INT AUTO_INCREMENT,
x DOUBLE(10, 3),
y DOUBLE(10, 3),
z DOUBLE(10, 3),
a DOUBLE(6, 3),
t DOUBLE(6, 3),
r DOUBLE(6, 3),
NU INT(6),
NV INT(6),
c0U DOUBLE(12, 6),
c0V DOUBLE(12, 6),
fx DOUBLE(12, 6),
fy DOUBLE(12, 6),
d1 DOUBLE(7, 6),
d2 DOUBLE(7,6),
d3 DOUBLE(7,6),
t1 DOUBLE(7,6),
t2 DOUBLE(7,6),
station_name VARCHAR(30),
serial_number INT(4),
camera_number VARCHAR(3),
calibration_date DATETIME,
coordinate_system CHAR(3),
PRIMARY KEY(id)
);
